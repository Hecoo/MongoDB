let mongoose = require("mongoose");

let usermodel = mongoose.model("Users", {
  text: {
    type: String,
    required: true,
    minlength: 1,
    trim: true,
  },
  completed: {
    type: Boolean,
    default: false,
  },
  completedAt: {
    type: Number,
    default: null,
  },
});

module.exports = {
  usermodel,
};
